    TEST(X86Decoder, getInstructionLength_Single) {
	    Context ctx;

	    for (size_t i = 0; i < ctx.getExpectedCount(); i++) {
		    std::vector<unsigned char> bytes = ctx.getAt(i);

		    ASSERT_EQ(bytes.size(), X86Utils::getInstructionLength(&bytes[0]));
	    }
    }

    TEST(X86Decoder, getInstructionLength_Stream) {
	    Context ctx;
	    std::vector<unsigned char> all = ctx.all();
	    size_t i = 0, offset = 0;

	    ASSERT_EQ(230, ctx.getExpectedCount());

	    for (; i < 230 /* hardcoded just to be sure */; i++) {
		    int size = X86Utils::getInstructionLength(&all[offset]);

		    ASSERT_EQ(ctx.getAt(i).size(), size);

		    offset += size;
	    }

	    ASSERT_EQ(offset, all.size());
    }
